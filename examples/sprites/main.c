#define cross_WIDTH 32/8
#define cross_HEIGHT 32
void main() {


	clearScreen(); // prepare screen
	signed char x = 20, y = 20;
	while (1){

		clearBuffer(); // clear drawing buffer
		fullPutSprite(x, y, cross_WIDTH, cross_HEIGHT, cross_DATA);
		swap();
		if (skClear == lastPressedKey()){
			break;
		}
		else if(skLeft == lastPressedKey()){
			x-=1;
		}
		else if(skRight == lastPressedKey()){
			x+=1;
		}
		else if(skUp == lastPressedKey()){
			y-=1;
			if (y < 0)
				y =0;

		}
		else if(skDown == lastPressedKey()){
			y+=1;
			if (y > YMAX-cross_HEIGHT)
				y = YMAX-cross_HEIGHT;
		}
	}




	PressAnyKey();

}